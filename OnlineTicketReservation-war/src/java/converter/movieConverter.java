package converter;

import bean.MovieManagedBean;
import entity.Movie;
import stub.MovieStub;
import java.util.logging.Level;
import java.util.logging.Logger;
import javax.faces.component.UIComponent;
import javax.faces.context.FacesContext;
import javax.faces.convert.Converter;
import javax.faces.convert.FacesConverter;

@FacesConverter("mConverter")
public class movieConverter implements Converter {
    private MovieStub stub;

    @Override
    public Object getAsObject(FacesContext facesContext, UIComponent component, String value) {
        if (value == null || value.length() == 0) {
            return null;
        }
        MovieManagedBean bean = (MovieManagedBean) facesContext.getApplication().getELResolver().
                getValue(facesContext.getELContext(), null, "movieBean");
        return bean.getMovieInfo(getKey(value));
    }

    java.lang.Integer getKey(String value) {
        java.lang.Integer key;
        key = Integer.valueOf(value);
        return key;
    }

    String getStringKey(java.lang.Integer value) {
        StringBuilder sb = new StringBuilder();
        sb.append(value);
        return sb.toString();
    }

    @Override
    public String getAsString(FacesContext facesContext, UIComponent component, Object object) {
        if (object == null) {
            return null;
        }
        if (object instanceof Movie) {
            Movie o = (Movie) object;
            return getStringKey(o.getMovieId());
        } else {
            Logger.getLogger(this.getClass().getName()).log(Level.SEVERE, "object {0} is of type {1}; expected type: {2}", new Object[]{object, object.getClass().getName(), Movie.class.getName()});
            return null;
        }
    }
}
